// Email form handling
const emailForm = document.querySelector('.cta-form');
const emailInput = document.querySelector('.email-input');
const submitBtn = document.querySelector('.cta-form .btn-primary');

if (emailForm) {
    emailForm.addEventListener('submit', (e) => {
        e.preventDefault();
        const email = emailInput.value;
        
        if (email && isValidEmail(email)) {
            // Simulate form submission
            submitBtn.innerHTML = '<i class="fas fa-check"></i> Added to Waitlist!';
            submitBtn.style.background = 'var(--chikoo-green)';
            emailInput.value = '';
            
            setTimeout(() => {
                submitBtn.innerHTML = '<i class="fas fa-rocket"></i> Get Early Access';
                submitBtn.style.background = '';
            }, 3000);
        } else {
            // Show error state
            emailInput.style.borderColor = '#ff4444';
            emailInput.placeholder = 'Please enter a valid email address';
            
            setTimeout(() => {
                emailInput.style.borderColor = '';
                emailInput.placeholder = 'Enter your email address';
            }, 3000);
        }
    });
}

// Email validation
function isValidEmail(email) {
    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    return emailRegex.test(email);
}

